a = float(input("Masukkan nilai a: "))
b = float(input("Masukkan nilai b: "))
i = float(input("Masukkan nilai c: "))
j = float(input("Masukkan nilai d: "))
x = float(input("Masukkan nilai e: "))
y = float(input("Masukkan nilai f: "))

hasil = (a - b) * (i / j) - (x + y)
print("{:.3f}".format(hasil))